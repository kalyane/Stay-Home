    var ref = firebase.database().ref('users');
    var users = $firebaseArray(ref);
    
    $scope.data = {};
    
    $scope.signup = function(){
        auth.$createUserWithEmailAndPassword($scope.data.email, $scope.data.password).then($scope.registerUser);
    }
    
